    private void BTNbuscar_carpetaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNbuscar_carpetaActionPerformed
        JFileChooser manejadorArchivo= new JFileChooser();
        if( manejadorArchivo == null ) manejadorArchivo = new JFileChooser();
           manejadorArchivo.setFileSelectionMode( JFileChooser.DIRECTORIES_ONLY);
        int seleccion = manejadorArchivo.showSaveDialog(BTNbuscar_carpeta);   
        String ruta="";
        if( seleccion == JFileChooser.APPROVE_OPTION ){
           ruta = manejadorArchivo.getSelectedFile().getAbsolutePath();    
           this.TFrutaReportes.setText(ruta);
        }
    }//GEN-LAST:event_BTNbuscar_carpetaActionPerformed

