    public void rellenaCampos(){
        this.TFusuario.setText(conf.getUsuario());
        this.TFcontraseña.setText(conf.getContraseña());
        this.TFesquema.setText(conf.getEsquema());
        this.TFrutaReportes.setText(conf.getRuta_Reportes());
        this.CMBmanejador.setSelectedIndex(conf.getManejador()-1);
    }
    
    
    public void GuardarConfiguración(){
        int man=0;
        if (this.CMBmanejador.getSelectedItem().toString().equals("MySQL"))
            man=1;
        else if (this.CMBmanejador.getSelectedItem().toString().equals("Oracle"))
            man=2;
        else
            man=3;
        Configuración nueva=new Configuración();        
          nueva.actualizar(this.TFusuario.getText(),this.TFcontraseña.getText(),this.TFesquema.getText(),man,this.TFrutaReportes.getText());          
          conf.guardarArchivo(nueva);
}
    public void AbrirConfiguración(){
        conf=conf.abrirArchivo();
        rellenaCampos();
    } 
